# Perulangam FOR
""" Perulangan for disebut sebagai counted loop atau perulangan yang terhitung, hal ini dikarenakan perulangan for biasanya digunakan pada kode yang sudah diketahui jumlah perulangannya """

angka = 10

for i in range(angka):
    print("Angka -", i)

# i merupakan bariable yang berfungsi untuk menampung indeks angka

# COntoh perapan For Looping pada list

Buah = ['jeruk', 'apel', 'melon', 'semangka']

for nama in Buah:
    print(nama)
